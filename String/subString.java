
//How to search last occurance of a substring inside a string?

package String;

public class subString {
    public static void main(String[] args) {
        String s1 = "India is my country. all indians are brothers and sisters. i love my country";
        System.out.println(s1.lastIndexOf("country"));
        System.out.println(s1.indexOf("country"));
        System.out.println(s1.length());
        System.out.println(s1.substring(0, 5));
    }

}
